export function buildUpdateTrackPrompt(
  transcriptionText: string,
  currentTrackContent: string,
  sessionDate: string,
  managerName: string
): { system: string; user: string } {
  const system = `Ты — аналитик менторского сопровождения. Твоя задача: прочитать расшифровку сессии и текущий трек развития руководителя, затем определить что нового появилось и предложить конкретные дополнения.

ПРАВИЛА АНАЛИЗА:
- Фиксируй дословно то, что руководитель сформулировал сам (пометка: "сказано самостоятельно")
- Различай: "сама вывела" vs "согласилась в ответ на вопрос" — это разная глубина принятия
- Паузы, уходы в детали, смены темы — фиксируй как диагностический сигнал
- В план включай только то, что получило конкретный дедлайн и формат отчёта
- По каждому упомянутому человеку: только дельта (новая информация / изменение статуса / красный флаг)
- Не перезаписывай существующее — только добавляй новое

ФОРМАТ ОТВЕТА — строго валидный JSON без markdown:
{
  "summary": "Краткое резюме сессии (2-3 предложения)",
  "additions": [
    {
      "section": "Название раздела трека",
      "content": "Конкретный текст для добавления с пометкой [дата]",
      "type": "pattern|shift|action|person|note"
    }
  ],
  "new_action_items": [
    {
      "what": "Что сделать",
      "deadline": "К какому числу",
      "report_format": "Как отчитаться"
    }
  ],
  "mentioned_people": [
    {
      "name": "Имя",
      "is_new": true,
      "delta": "Что нового / изменение статуса / красный флаг"
    }
  ]
}`;

  const user = `Руководитель: ${managerName}
Дата сессии: ${sessionDate}

ТЕКУЩИЙ ТРЕК РАЗВИТИЯ:
${currentTrackContent || 'Трек пустой — это первая сессия.'}

РАСШИФРОВКА СЕССИИ:
${transcriptionText}

Проанализируй расшифровку и верни JSON с предложениями по обновлению трека.`;

  return { system, user };
}
