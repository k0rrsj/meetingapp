export function buildTranscriptionPrompt(scenario: string): { system: string; user: string } {
  const system = `На основе сценария встречи сформируй промпт для AI-расшифровки записи встречи.
Промпт должен попросить AI извлечь:
- Ключевые факты и решения
- Проблемы и сигналы
- Договорённости и следующие шаги
- Оценку сильных и слабых сторон руководителя

Формат: готовый промпт на русском языке, который можно скопировать и вставить в AI-инструмент.`;

  const user = `Сценарий встречи:
${scenario}

Сформируй промпт для расшифровки записи этой встречи.`;

  return { system, user };
}
