const SCENARIO_RULES_FROM_MEETING_3 = `

### Обязательно для встречи №3 и далее (номер этой встречи ≥ 3)
**Приоритет сюжетов.** В начале блока «## 3. ОСНОВНОЙ БЛОК» сначала подзаголовок «### Приоритет сюжетов на эту встречу». Назови ровно **два** сюжета — не три, не список «всего важного». Для каждого: короткое название + 2–4 предложения обоснования, почему именно сейчас этот сюжет важнее остального из контекста. Остальные темы сознательно не бери в работу на эту сессию.

**Договорённости.** В блоке «## 4. ФИКСАЦИЯ» у **каждой** договорённости обязательно две отдельные строки (или подпункта): «**Дедлайн:** …» (конкретная дата или «до пятницы N недели») и «**Формат отчёта:** …» (как именно отчитаться: чат, письмо, звонок, таблица, что написать). Без обеих строк договорённость недопустима.`;

export function buildScenarioPrompt(
  previousContextText: string | null,
  managerProfile: {
    name: string;
    position: string | null;
    context: string | null;
    directorRequest: string | null;
    strengths: string | null;
    weaknesses: string | null;
    consultantComments: string | null;
  },
  meetingNumber?: number | null,
): { system: string; user: string } {
  const fromThird = typeof meetingNumber === 'number' && meetingNumber >= 3;

  const system = `Ты помогаешь управленческому консультанту подготовить сценарий сессии one-to-one.

Сформируй сценарий строго по следующей структуре — 4 блока. Все 4 блока обязательны. Не обрывай текст на середине — каждый сюжет и каждый блок должны быть завершены полностью.

## 1. ЧЕК-ИН (5 минут)
Один конкретный вопрос про энергетическое состояние руководителя.
Не общий ("как дела?"), а точный — про состояние, настрой, нагрузку прямо сейчас.

## 2. ПРОВЕРКА ДОГОВОРЁННОСТЕЙ (10 минут)
Список конкретных пунктов из последней сессии — что было обещано, что нужно проверить.
Формулировки вопросов: не "почему не сделала", а "что случилось в момент, когда должна была начать".
Фиксируй: сделано / частично / не начато.

## 3. ОСНОВНОЙ БЛОК (25–30 минут)
Один-два диагностических сюжета. Приоритет: незавершённые циклы и нарастающие риски.
Для каждого сюжета обязательно: кейс → диагностический вопрос → анализ → конкретный следующий шаг → фиксация итога сюжета.
Каждый сюжет должен быть завершён — не обрывай на полуслове.

## 4. ФИКСАЦИЯ (5 минут)
ОБЯЗАТЕЛЬНЫЙ БЛОК. Без него сценарий считается незавершённым.
Не более 3 договорённостей. Каждая — с конкретным дедлайном и форматом отчёта.
Формат каждой договорённости: "что сделать / к какому числу / как отчитаться".
Пример: "Выбрать одну задачу для делегирования Тиграну — до пятницы — написать в чат".
У каждой договорённости явно укажи **Дедлайн** и **Формат отчёта** (отдельными строками).

Правила:
- Не используй общие фразы и шаблонные вопросы
- Каждый пункт должен опираться на конкретный контекст этого руководителя
- Язык: русский, деловой, конкретный
- Сценарий должен быть написан полностью — от блока 1 до блока 4 включительно${fromThird ? SCENARIO_RULES_FROM_MEETING_3 : ''}`;

  const profileLines: string[] = [];
  if (managerProfile.position) profileLines.push(`Должность: ${managerProfile.position}`);
  if (managerProfile.context) profileLines.push(`Контекст: ${managerProfile.context}`);
  if (managerProfile.directorRequest) profileLines.push(`Запрос от директора: ${managerProfile.directorRequest}`);
  if (managerProfile.strengths) profileLines.push(`Сильные стороны: ${managerProfile.strengths}`);
  if (managerProfile.weaknesses) profileLines.push(`Слабые стороны: ${managerProfile.weaknesses}`);
  if (managerProfile.consultantComments) profileLines.push(`Заметки консультанта: ${managerProfile.consultantComments}`);

  const meetingLine =
    typeof meetingNumber === 'number'
      ? `Номер предстоящей встречи: ${meetingNumber}.${fromThird ? ' Применяй правила для встречи №3 и далее (приоритет топ-2 сюжетов, дедлайн и формат отчёта по каждой договорённости).' : ''}`
      : '';

  const trimmedPrev = previousContextText?.trim() ?? '';
  const isFirstMeeting = meetingNumber === 1;

  let contextIntro: string;
  if (trimmedPrev) {
    contextIntro = isFirstMeeting
      ? `Вводной контекст перед первой сессией (факты до трека, не из прошлых встреч):\n${trimmedPrev}`
      : `Контекст предыдущих встреч:\n${trimmedPrev}`;
  } else if (isFirstMeeting) {
    contextIntro =
      'Это первая сессия с данным руководителем: прошлых встреч в треке ещё нет. Опирайся на профиль руководителя и методологию выше; вводной текст консультант мог не заполнять — этого достаточно.';
  } else {
    contextIntro = 'Предыдущих встреч нет — это первая сессия с данным руководителем.';
  }

  const firstMeetingHint = isFirstMeeting
    ? '\n\nУчти: это первая встреча — формальных договорённостей с прошлых сессий ещё нет; во 2-м блоке сценария сфокусируйся на ожиданиях, запросе директора и договорённостях «на старт», если они есть во вводном контексте или профиле.'
    : '';

  const user = `${contextIntro}

${meetingLine ? `${meetingLine}\n` : ''}${profileLines.length > 0 ? `Дополнительный профиль:\n${profileLines.join('\n')}` : ''}${firstMeetingHint}

Подготовь сценарий сессии по структуре выше.`;

  return { system, user };
}
